    public int cek(ArrayList<String> x, String m) {
        int j = 0;
        for (int i = 0; i < x.size(); i++) {
            String data = x.get(i);
            if (data == m) {
                j = i;
            }
        }
        return j;

    }

    public void tambah(ArrayList<String> listMenu,ArrayList<String> hasil,ArrayList<String> x,ArrayList<String> y,ArrayList<String> z){
        for (int i=0;i<x.size();i++){
            String dataX=x.get(i);
            String dataY=y.get(i);
            String dataZ=z.get(i);
            hasil.add(index(listMenu,dataX)+";"+dataX+";"+dataY+";"+dataZ);
        }
    }

    public int index(ArrayList<String> list,String masakan){
        int j=0;
        for (int i=0;i<list.size();i++){
            String[] hasil=list.get(i).split("-");
            if (hasil[1].equals(masakan)){
                j=Integer.parseInt(hasil[0]);
            }
        }
        return j;
    }
