    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {
            com.ubaya.projectdistprog.MenuWS_Service service = new com.ubaya.projectdistprog.MenuWS_Service();
            com.ubaya.projectdistprog.MenuWS port = service.getMenuWSPort();

            ArrayList<String> listMenu = (ArrayList<String>) port.displayMenuRestoran(rest.split("-")[0]);
            menuPilihan= new ArrayList<>();
            String masakan = (String) cbMenu.getSelectedItem();
            int jumlah = (int) jSpinnerJumlah.getValue();
            if (lmasakan.contains(masakan)) {
                int c = cek(lmasakan, masakan);
                jum.set(c, String.valueOf(Integer.parseInt(jum.get(c)) + jumlah));
            } else {
                lmasakan.add(masakan);
                jum.add(String.valueOf(jumlah));
                lharga.add(txtHarga.getText());
            }
            tambah(listMenu,menuPilihan,lmasakan,jum,lharga);
            DefaultTableModel model = (DefaultTableModel) tableMenu.getModel();

            model.setRowCount(0);
            for (int i = 0; i < menuPilihan.size(); i++) {
                Object[] data = {
                    menuPilihan.get(i).split(";")[1], menuPilihan.get(i).split(";")[2]
                };
                model.addRow(data);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

