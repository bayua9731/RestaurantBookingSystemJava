    public void RefreshTable() {
        com.ubaya.projectdistprog.BookingWS_Service service = new com.ubaya.projectdistprog.BookingWS_Service();
        com.ubaya.projectdistprog.BookingWS port = service.getBookingWSPort();

        DefaultTableModel model = (DefaultTableModel) tableBooking.getModel();
        model.setRowCount(0);
        Object[] rowData = new Object[7];

        String restoranku = restoran.get(0);
        String[] dataResKu = restoranku.split("-");
        ArrayList<String> listModel = (ArrayList<String>) port.displayBookingRestaurant(dataResKu[0], "tidak", "");

        for (int i = 0; i < listModel.size(); i++) {
            String[] data = listModel.get(i).split(";");
            String x = "Tidak";
            rowData[0] = data[0];
            rowData[1] = data[13];
            rowData[2] = data[2];
            rowData[3] = data[4];
            rowData[4] = data[1];
            rowData[5] = data[3];
            String[] datamakan = listModel.get(i).split("!makan!");
            if (datamakan.length == 2) {
                x = "Ada";
            }
            rowData[6] = x;

            model.addRow(rowData);

        }
    }

