    private void tableBookingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBookingMouseClicked
        // TODO add your handling code here:
        try {
            int baris = tableBooking.getSelectedRow();
            TableModel model = tableBooking.getModel();
            String id = (model.getValueAt(baris, 0).toString());
            String keterangan = (model.getValueAt(baris, 5).toString());
            String cekPo = (model.getValueAt(baris, 6).toString());
            System.out.println(cekPo);
            if (cekPo.equals("Tidak")) {
                if (keterangan.equals("sudahpesan")) {
                    int dialog = JOptionPane.showConfirmDialog(null,
                            "Apakah Tamu Sudah Datang ?",
                            "Pesan Informasi",
                            JOptionPane.YES_NO_OPTION);

                    if (dialog == JOptionPane.YES_OPTION) {
                        com.ubaya.projectdistprog.BookingWS_Service service = new com.ubaya.projectdistprog.BookingWS_Service();
                        com.ubaya.projectdistprog.BookingWS port = service.getBookingWSPort();
                        port.updateKeteranganBooking(id);
                        RefreshTable();
                    }
                } else if (keterangan.equals("sudahdatang")) {
                    int dialog = JOptionPane.showConfirmDialog(null,
                            "Apakah Tamu Sudah Selesai ?",
                            "Pesan Informasi",
                            JOptionPane.YES_NO_OPTION);

                    if (dialog == JOptionPane.YES_OPTION) {
                        com.ubaya.projectdistprog.BookingWS_Service service = new com.ubaya.projectdistprog.BookingWS_Service();
                        com.ubaya.projectdistprog.BookingWS port = service.getBookingWSPort();
                        port.updateKeteranganBooking(id);
                        RefreshTable();
                    }
                }
            } else {
                if (keterangan.equals("sudahpesan")) {
                    int dialog = JOptionPane.showConfirmDialog(null,
                            "Apakah ingin melihat Pre Order ?",
                            "Pesan Informasi",
                            JOptionPane.YES_NO_OPTION);

                    if (dialog == JOptionPane.YES_OPTION) {
                        FormLihatPO formLihat = new FormLihatPO();
                        formLihat.restoranP = restoran.get(0);
                        formLihat.bookingId=id;
                        formLihat.setVisible(true);
                    }
                } else if (keterangan.equals("sudahdatang")) {
                    com.ubaya.projectdistprog.BookingWS_Service service = new com.ubaya.projectdistprog.BookingWS_Service();
                    com.ubaya.projectdistprog.BookingWS port = service.getBookingWSPort();
                    port.updateKeteranganBooking(id);
                    RefreshTable();
                }

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_tableBookingMouseClicked

